pub fn add_f64(num_1: &f64, num_2: &f64) -> f64 {
    return num_1+num_2;
}

pub fn subtract_f64(num_1: &f64, num_2: &f64) -> f64 {
    return num_1-num_2;
}

pub fn mult_f64(num_1: &f64, num_2: &f64) -> f64 {
    return num_1*num_2;
}

pub fn divi_f64(num_1: &f64, num_2: &f64) -> f64 {
    return num_1/num_2;
}